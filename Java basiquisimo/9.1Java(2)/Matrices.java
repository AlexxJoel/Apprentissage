
package matrices;
import java.util.*;
public class Matrices {

    public static void main(String[] args) {
        int[][] matriz={{2,4,4},{6,6,9},{8,10,12}};
    
    for(int x=0;x <matriz.length;x++){
        for(int y=0; y <matriz.length;y++){
            System.out.println(matriz [x][y]);
    }
}
        
    }
    
}
